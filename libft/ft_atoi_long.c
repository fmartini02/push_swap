#include "libft.h"

long int	ft_atoi_long(char *s)
{
	long int	sign;
	long int	nmb;

	sign = 1;
	nmb = 0;
	while (*s == 32 || (*s >= 9 && *s <= 13))
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			sign = -1;
		s++;
	}
	while (*s && (*s >= '0' && *s <= '9'))
	{
		nmb = nmb * 10 + (*s++ - '0');
	}
	return (nmb * sign);
}
